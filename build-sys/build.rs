use std::env;
use std::fs;
use std::path::Path;


fn main() {
    println!("cargo:rerun-if-changed=build.rs");

    // Check if "default_final_build_zark" folder exists, create if it doesn't
    let final_build_path = Path::new("default_final_build_zark");
    if !final_build_path.exists() {
        fs::create_dir(final_build_path).expect("Failed to create default_final_build_zark folder");
        println!("Created default_final_build_zark folder");
    } else {
        println!("default_final_build_zark folder already exists");
    }

    // Print the final build path for use in the main build process
    println!("cargo:rustc-env=ZARK_WAF_FINAL_BUILD_PATH={}", final_build_path.display());

    println!("Build script completed successfully.");
}